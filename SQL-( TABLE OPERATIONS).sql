FROM MOBILECOMPANY1;

#LEFT JOIN 

SELECT MOBILECOMPANY1.NAME,MOBILECOMPANY2.GRADE
FROM MOBILECOMPANY1
LEFT JOIN MOBILECOMPANY2
ON MOBILECOMPANY1.ID = MOBILECOMPANY2.ID;

# RIGHT JOIN

SELECT MOBILECOMPANY2.NAME,MOBILECOMPANY1.AGE
FROM MOBILECOMPANY2
RIGHT JOIN MOBILECOMPANY1
ON MOBILECOMPANY2.ID = MOBILECOMPANY1.ID;

# FULL JOIN

SELECT MOBILECOMPANY1.NAME,MOBILECOMPANY2.AGE,MOBILECOMPANY1.SECTION,MOBILECOMPANY2.GRADE
FROM MOBILECOMPANY1
FULL OUTER JOIN MOBILECOMPANY2 ON MOBILECOMPANY2.ID = MOBILECOMPANY1.ID
ORDER BY MOBILECOMPANY1.NAME;

#UNION

SELECT NAME FROM MOBILECOMPANY1
