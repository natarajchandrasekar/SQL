#MIN AND MAX

SELECT MIN(SALARY) AS MINSALARY
FROM MOBILECOMPANY1;
SELECT MAX(SALARY) AS MAXSALARY
FROM MOBILECOMPANY1;

# COUNTING THE COLUMN SECTION

